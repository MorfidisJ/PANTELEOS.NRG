let currentLang = 'el'; // Greek default per user request

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  window.currentLang = lang;
  localStorage.setItem('panteleos_lang', lang);
  document.documentElement.lang = lang;

  // Update language selector buttons
  const langBtns = document.querySelectorAll('.lang-switch button');
  langBtns.forEach(btn => {
    const isTarget = btn.getAttribute('data-lang') === lang;
    btn.classList.toggle('active', isTarget);
    btn.setAttribute('aria-pressed', isTarget ? 'true' : 'false');
  });

  // Apply DOM Mappings
  DOM_MAPPINGS.forEach(item => {
    const els = document.querySelectorAll(item.selector);
    els.forEach(el => {
      if (item.key && TRANSLATIONS[lang][item.key]) {
        const val = TRANSLATIONS[lang][item.key];
        el.innerHTML = window.replaceEmojisWithSVG ? window.replaceEmojisWithSVG(val) : val;
      }
      if (item.labelKey && TRANSLATIONS[lang][item.labelKey]) {
        el.setAttribute('data-label', TRANSLATIONS[lang][item.labelKey]);
      }
      if (item.placeholderKey && TRANSLATIONS[lang][item.placeholderKey]) {
        el.setAttribute('placeholder', TRANSLATIONS[lang][item.placeholderKey]);
      }
    });
  });

  // Apply translations to all DOM elements with explicit data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (TRANSLATIONS[lang][key]) {
      const val = TRANSLATIONS[lang][key];
      el.innerHTML = window.replaceEmojisWithSVG ? window.replaceEmojisWithSVG(val) : val;
    }
  });

  // Apply translations to placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (TRANSLATIONS[lang][key]) {
      el.setAttribute('placeholder', TRANSLATIONS[lang][key]);
    }
  });

  // Hook for Virtual Engineering Assistant (Chatbot)
  if (window.updateChatbotLanguage) {
    window.updateChatbotLanguage(lang);
  }
  if (window.updateGaugerLanguage) {
    window.updateGaugerLanguage(lang);
  }
  if (window.updateTeamModalLanguage) {
    window.updateTeamModalLanguage(lang);
  }
  if (window.updateAccessibilityLanguage) {
    window.updateAccessibilityLanguage(lang);
  }
  if (window.updateCareersLanguage) {
    window.updateCareersLanguage(lang);
  }
  if (window.replaceEmojisInDOM) {
    window.replaceEmojisInDOM();
  }
}

document.addEventListener('DOMContentLoaded', () => {
  // Check if language was previously selected by user, otherwise default to Greek ('el') per user request
  const savedLang = localStorage.getItem('panteleos_lang') || 'el';
  
  // Attach click listeners to language selector
  const langSwitch = document.getElementById('lang-switch');
  if (langSwitch) {
    langSwitch.addEventListener('click', (e) => {
      const btn = e.target.closest('button[data-lang]');
      if (btn) {
        const targetLang = btn.getAttribute('data-lang');
        setLanguage(targetLang);
      }
    });
  }

  // Initialize default language
  setLanguage(savedLang);
});
